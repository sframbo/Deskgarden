package background;

public class Decor {
	private int locID;
	private int type;
	
	public Decor(int type, int locID) {
		
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public int getLocID() {
		return locID;
	}

	public void setLocID(int locID) {
		this.locID = locID;
	}
}
